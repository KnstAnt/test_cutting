//! ### 1. Аудит математики и логики: Всё ли стабильно?
//! 
//! **А. Защита от деления на ноль в `intersect_edge`**
//! ```rust
//! let t = d_a / (d_a - d_b);
//! ```
//! **Вердикт:** Безопасно. 
//! Мы вызываем эту функцию *только* если знаки `d_a` и `d_b` разные (одна точка строго $>0$, другая $\le 0$). Следовательно, `d_a - d_b` математически никогда не может быть равно нулю. Ошибки `NaN` или `Infinity` здесь исключены.
//! 
//! **Б. Проблема точности (Вершины лежащие ровно на плоскости $d = 0.0$)**
//! Если плоскость проходит идеально через вершину, расстояние $d = 0.0$.
//! В вашей логике: `filter(|&&(_, d)| d > 0.0)`. Значит, $d=0$ считается "под водой" (false).
//! Если у треугольника одна вершина $>0$, а две другие (или одна из них) ровно $0.0$, алгоритм попытается найти пересечение. В результате `t = 0`, и новые вершины совпадут со старыми. Сгенерируются **вырожденные треугольники** (с нулевой площадью) и вырожденные отрезки.
//! **Вердикт:** Безопасно, но стоит знать. Вырожденные фигуры не ломают программу, их объем и моменты инерции равны нулю, поэтому они просто растворятся в формуле Остроградского-Гаусса, не исказив итоговый результат.
//! 
//! **В. Критическое требование к входным данным (Ориентация нормалей)**
//! Ваш алгоритм закрытия «крышки» принудительно заставляет нормаль крышки смотреть вверх (из воды):
//! `let is_pointing_out = cross.dot(&plane.normal) > 0.0;`
//! Это блестящее решение, но оно **жестко требует, чтобы все треугольники исходного CAD-меша судна смотрели нормалями наружу** (стандартная watertight-модель). Если вам на вход подадут вывернутый наизнанку меш судна, интеграл родных граней даст отрицательный объем, а крышка — положительный. Они вычтутся, и водоизмещение будет посчитано неверно (взятие `abs()` в конце тут не спасет).
//! **Рекомендация:** Убедитесь, что 3D-модели проходят санитизацию перед загрузкой (нормали смотрят наружу корпуса). Алгоритм написан исходя из идеальной топологии корпуса.
//! 
//! **Г. Судно полностью под водой или полностью в воздухе**
//! * В воздухе: `submerged_triangles` пусто, `total_volume = 0.0`. Центр возвращается как `Point3::origin()`. Защита от деления на ноль сработала.
//! * Под водой: Срезов нет (`waterline_edges` пуст). Крышка не строится, берется объем всего меша. Всё верно.
//! 
